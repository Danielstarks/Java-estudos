public class DadosPrimitivo {

    public static void main(String[] args) {

        /* Tipos primitivos
        
         Inteiros: byte, short, int, long, char.
         Pontos flutuantes: float e double.
         Boolean: true ou false.  
        
         */
        
        byte a = 10; short b = 20; int  c = 30; long d = 40;
        float e = 3.4f; double f = 5.4;
        System.out.println("valor de inteiros: " + a+b+c+d);
        System.out.println("valor de pontos flutuantes: " +e+" " +f);   
        
        
        
        
        
        
        
        
        
        
        
        
    }

}
